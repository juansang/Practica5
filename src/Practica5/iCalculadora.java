package Practica5;

public interface iCalculadora {

    /**
     * declaración método suma()
     * @param x,y
     */
    public abstract int suma(int x, int y);

    /**
     * declaración método resta()
     * @param x,y
     */
    public abstract int resta(int x, int y);

    /**
     * declaración método multiplicacion()
     * @param x,y
     */
    public abstract int multiplicacion(int x, int y);

    /**
     * declaración método division()
     * @param x,y
     */
    public abstract int division(int x, int y);
}
